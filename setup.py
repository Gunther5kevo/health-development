def setup_virtual_environment():
    """Set up Python virtual environment"""
    print("\nğŸ Setting up virtual environment...")
    
    if not os.path.exists('venv'):
        try:
            subprocess.run([sys.executable, '-m', 'venv', 'venv'], check=True)
            print("   âœ… Virtual environment created")
        except subprocess.CalledProcessError:
            print("   âŒ Failed to create virtual environment")
            return False
    else:
        print("   â„¹ï¸  Virtual environment already exists")
    
    return True

def install_dependencies():
    """Install Python dependencies"""
    print("\nğŸ“¦ Installing Python dependencies...")
    
    # Determine the correct pip path
    if os.name == 'nt':  # Windows
        pip_path = os.path.join('venv', 'Scripts', 'pip')
    else:  # Unix/Linux/Mac
        pip_path = os.path.join('venv', 'bin', 'pip')
    
    try:
        # Upgrade pip first
        subprocess.run([pip_path, 'install', '--upgrade', 'pip'], check=True)
        
        # Install requirements
        subprocess.run([pip_path, 'install', '-r', 'backend/requirements.txt'], check=True)
        print("   âœ… Dependencies installed successfully")
        return True
    except subprocess.CalledProcessError as e:
        print(f"   âŒ Failed to install dependencies: {e}")
        return False
    except FileNotFoundError:
        print("   âŒ Virtual environment not found. Please activate it manually.")
        return False

